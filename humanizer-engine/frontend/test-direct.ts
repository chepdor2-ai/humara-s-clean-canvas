// ─── Utility functions ───
function countWords(text: string): number {
  return text.split(/\s+/).filter(Boolean).length;
}

function findContractions(text: string): string[] {
  return text.match(/\b\w+'(?:t|s|re|ve|ll|d|m)\b/gi) || [];
}

function findQuestions(text: string): string[] {
  return text.match(/[A-Za-z][^.!?]*\?/g) || [];
}

function findFirstPerson(text: string): string[] {
  return text.match(/\b(I|me|my|mine|myself|we|us|our|ours|ourselves)\b/g) || [];
}

function sentenceCount(text: string): number {
  return text.split(/(?<=[.!?])\s+/).filter(s => s.trim().length > 0).length;
}

function compareSentences(input: string, output: string): void {
  const inputSents = input.split(/(?<=[.!?])\s+/).filter(s => s.trim().length > 5);
  const outputSents = output.split(/(?<=[.!?])\s+/).filter(s => s.trim().length > 5);

  console.log(`  Input sentences: ${inputSents.length}`);
  console.log(`  Output sentences: ${outputSents.length}`);

  // Check for identical sentences (no change)
  let identical = 0;
  for (const os of outputSents) {
    if (inputSents.some(is => is.trim() === os.trim())) {
      identical++;
    }
  }
  console.log(`  Identical (unchanged) sentences: ${identical}/${outputSents.length}`);
}

